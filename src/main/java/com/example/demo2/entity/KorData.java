package com.example.demo2.entity;

public class KorData {

    private int Number;
    private String CAR;
    private String KCAL;
    private String FAT;
    private String Na;
    private String Name;
    private String PRO;
    private String SUG;
    private String LCAT;
    private String SCAT;

    public int getNumber() {
        return Number;
    }

    public void setNumber(int number) {
        Number = number;
    }

    public String getCAR() {
        return CAR;
    }

    public void setCAR(String CAR) {
        this.CAR = CAR;
    }

    public String getKCAL() {
        return KCAL;
    }

    public void setKCAL(String KCAL) {
        this.KCAL = KCAL;
    }

    public String getFAT() {
        return FAT;
    }

    public void setFAT(String FAT) {
        this.FAT = FAT;
    }

    public String getNa() {
        return Na;
    }

    public void setNa(String na) {
        Na = na;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getPRO() {
        return PRO;
    }

    public void setPRO(String PRO) {
        this.PRO = PRO;
    }

    public String getSUG() {
        return SUG;
    }

    public void setSUG(String SUG) {
        this.SUG = SUG;
    }

    public String getLCAT() {
        return LCAT;
    }

    public void setLCAT(String LCAT) {
        this.LCAT = LCAT;
    }

    public String getSCAT() {
        return SCAT;
    }

    public void setSCAT(String SCAT) {
        this.SCAT = SCAT;
    }
}
