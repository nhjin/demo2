package com.example.demo2.entity;

public class Nutrition {

    private float CAR;
    private float FAT;
    private int ID;
    private float KCAL;
    private float NA;
    private float PRO;
    private float SUG;

    public float getCAR() {
        return CAR;
    }

    public void setCAR(float CAR) {
        this.CAR = CAR;
    }

    public float getFAT() {
        return FAT;
    }

    public void setFAT(float FAT) {
        this.FAT = FAT;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public float getKCAL() {
        return KCAL;
    }

    public void setKCAL(float KCAL) {
        this.KCAL = KCAL;
    }

    public float getNA() {
        return NA;
    }

    public void setNA(float NA) {
        this.NA = NA;
    }

    public float getPRO() {
        return PRO;
    }

    public void setPRO(float PRO) {
        this.PRO = PRO;
    }

    public float getSUG() {
        return SUG;
    }

    public void setSUG(float SUG) {
        this.SUG = SUG;
    }
}
